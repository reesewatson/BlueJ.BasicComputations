
public class ShortCalculator {
      
        public static short subtraction(short a, short b) {
            return (short) (a - b); 
        }
        
        public static short addition(short a, short b) {
            return (short) (a + b); 
        }
         
        public static short multiplication(short a, short b) {
            return (short) (a * b); 
        }
            
        public static short division(short a, short b) {
            return (short) (a / b); 
        }
            
        public static short modulo(short a, short b) {
            return (short) (a % b); 
        }
    
}
